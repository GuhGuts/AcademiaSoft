#Faça um programa que que solicite ao usuário, para que digite 4 notas e calcule a média artimética.

nota1 = float(input("Digite 1ª nota: "))
nota2 = float(input("Digite 2ª nota: "))
nota3 = float(input("Digite 3ª nota: "))
nota4 = float(input("Digite 4ª nota: "))

media = (nota1 + nota2 + nota3 + nota4) / 4

print("Média final: ", media)